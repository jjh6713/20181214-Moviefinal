package movie;

public class MovieItem {
	private String item_name;
	private String item_director;
	private String item_actor;
	private String item_info;
	private String item_date;
	private String item_link;
	private String item_image;
	
	public String getItem_name() {
		return item_name;
	}
	public void setItem_name(String item_name) {
		this.item_name = item_name;
	}
	public String getItem_director() {
		return item_director;
	}
	public void setItem_director(String item_director) {
		this.item_director = item_director;
	}
	public String getItem_actor() {
		return item_actor;
	}
	public void setItem_actor(String item_actor) {
		this.item_actor = item_actor;
	}
	public String getItem_info() {
		return item_info;
	}
	public void setItem_info(String item_info) {
		this.item_info = item_info;
	}
	public String getItem_date() {
		return item_date;
	}
	public void setItem_date(String item_date) {
		this.item_date = item_date;
	}
	public String getItem_link() {
		return item_link;
	}
	public void setItem_link(String item_link) {
		this.item_link = item_link;
	}
	public String getItem_image() {
		return item_image;
	}
	public void setItem_image(String item_image) {
		this.item_image = item_image;
	}
}
