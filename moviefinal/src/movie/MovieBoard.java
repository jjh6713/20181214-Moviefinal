package movie;

public class MovieBoard {
		private String board_no;
		private String board_rating;
		private String board_review;
		private String board_date;
		private String member_id;
		private String item_name;
		
		public String getBoard_no() {
			return board_no;
		}
		public void setBoard_no(String board_no) {
			this.board_no = board_no;
		}
		public String getBoard_rating() {
			return board_rating;
		}
		public void setBoard_rating(String board_rating) {
			this.board_rating = board_rating;
		}
		public String getBoard_review() {
			return board_review;
		}
		public void setBoard_review(String board_review) {
			this.board_review = board_review;
		}
		public String getBoard_date() {
			return board_date;
		}
		public void setBoard_date(String board_date) {
			this.board_date = board_date;
		}
		public String getMember_id() {
			return member_id;
		}
		public void setMember_id(String member_id) {
			this.member_id = member_id;
		}
		public String getItem_name() {
			return item_name;
		}
		public void setItem_name(String item_name) {
			this.item_name = item_name;
		}
}
